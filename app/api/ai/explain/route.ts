export async function POST(request: NextRequest) {
  try {
    const { term, context } = await request.json();
    
    // Проверяем кэш
    const cached = await prisma.termCache.findFirst({
      where: {
        term: term,
        context: context?.substring(0, 100)
      }
    });
    
    if (cached) {
      return NextResponse.json({ explanation: cached.explanation });
    }
    
    // Ищем в словаре
    let explanation = termDictionary[term];
    
    if (!explanation) {
      // Если не нашли - генерируем базовое объяснение
      explanation = `${term} - термин из мира Teyvat. `;
      if (context) {
        explanation += `Встречается в контексте: "${context.substring(0, 100)}...". `;
      }
      explanation += `Для получения более подробной информации об этом термине, пожалуйста, обратитесь к официальным источникам Genshin Impact.`;
    }
    
    // Сохраняем в кэш
    await prisma.termCache.create({
      data: {
        term,
        context: context?.substring(0, 100),
        explanation
      }
    });
    
    return NextResponse.json({ explanation });
  } catch (error) {
    return NextResponse.json(
      { error: 'Не удалось получить объяснение' },
      { status: 500 }
    );
  }
}